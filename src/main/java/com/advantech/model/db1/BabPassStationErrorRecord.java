package com.advantech.model.db1;

import java.io.Serializable;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.Table;

/**
 *
 * @author Wei.Cheng
 */
@Entity
@Table(name = "BabPassStationErrorRecord")
public class BabPassStationErrorRecord implements Serializable {

    private int id;
    private BabPassStationRecord babPassStationRecord;
    private String reason;

    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    @Column(name = "id", unique = true, nullable = false)
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @ManyToOne(fetch = FetchType.LAZY)
    @JoinColumn(name = "babPassStationRecord_id", nullable = false)
    public BabPassStationRecord getBabPassStationRecord() {
        return babPassStationRecord;
    }

    public void setBabPassStationRecord(BabPassStationRecord babPassStationRecord) {
        this.babPassStationRecord = babPassStationRecord;
    }

    @Column(name = "reason")
    public String getReason() {
        return reason;
    }

    public void setReason(String reason) {
        this.reason = reason;
    }

}
